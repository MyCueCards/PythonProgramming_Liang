import turtle

r = eval(input("Enter the radius of the circle: "))
r2 = r * 2

turtle.penup()
turtle.goto(0-r, 0)
turtle.pendown()
turtle.circle(r)

turtle.penup()
turtle.goto(0+r, 0)
turtle.pendown()
turtle.circle(r)

turtle.penup()
turtle.goto(0-r, 0-r2)
turtle.pendown()
turtle.circle(r)

turtle.penup()
turtle.goto(0+r, 0-r2)
turtle.pendown()
turtle.circle(r)

turtle.done()
